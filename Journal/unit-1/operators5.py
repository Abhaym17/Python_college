# Arithmatic operators
x=2
y=4

print("Arithmatic operators")
print("x + y :",x+y)
print("x - y :",x-y)
print("x * y :",x*y)
print("x ** y :",x**y)
print("x / y :",x/y)
print("x // y :",x//y)
print("x % y :",x%y)
print("")

