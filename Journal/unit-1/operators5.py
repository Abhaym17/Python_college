# Bitwise Operators
m = 5  # 0b0101
n = 3  # 0b0011
print("Bitwise Operators:")
print("m & n:", m & n)
print("m | n:", m | n)
print("m ^ n:", m ^ n)
print("~m:", ~m)
print("m << 1:", m << 1)
print("m >> 1:", m >> 1)
